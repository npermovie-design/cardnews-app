// Supabase에 삽입
async function seed() {
  const SUPABASE_URL = process.env.VITE_SUPABASE_URL || process.env.SUPABASE_URL;
  const SUPABASE_KEY = process.env.VITE_SUPABASE_ANON_KEY || process.env.SUPABASE_ANON_KEY;

  if (!SUPABASE_URL || !SUPABASE_KEY) {
    console.log("Supabase 환경변수가 없습니다. localStorage용 JSON을 출력합니다.\n");
    // localStorage용 JSON 출력
    BRIEFINGS.forEach(b => {
      console.log(`localStorage.setItem("nper_sns_briefing_${b.date}", ${JSON.stringify(JSON.stringify(b))});`);
    });
    return;
  }

  const { createClient } = await import("@supabase/supabase-js");
  const supabase = createClient(SUPABASE_URL, SUPABASE_KEY);

  for (const b of BRIEFINGS) {
    const { error } = await supabase.from("sns_news").upsert({
      id: `briefing_${b.date}`,
      title: b.title,
      content: b.content,
      category: "briefing",
      platforms: ["instagram", "youtube", "tiktok", "naver"],
      author_uid: "system_seed",
      pinned: false,
      views: 0,
      summary: `${b.date.replace(/-/g, ".")} SNS 마케팅 주요 뉴스 7선`,
      created_at: `${b.date}T07:00:00+09:00`,
    }, { onConflict: "id" });

    if (error) console.error(`${b.date} 실패:`, error.message);
    else console.log(`${b.date} 저장 완료`);
  }
  console.log("\n시드 완료! 총", BRIEFINGS.length, "개 브리핑");
}

seed();
